namespace Dapper
{
    /// <summary>
    /// Enumeration for defining the sort order of a query.
    /// </summary>
    public enum SortOrder
    {
        /// <summary>
        /// Sort the results in ascending order
        /// </summary>
        Ascending = 0,

        /// <summary>
        /// Sort the results in descending order
        /// </summary>
        Descending = 1
    }
}
